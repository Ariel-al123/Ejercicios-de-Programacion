package operaciones;

public class multiplicacion {
    double a;
    double b;

    public multiplicacion(double a, double b) {
        this.a = a;
        this.b = b;
    }
    //Getters
    public int getmultiplicacion(int a, int b) { return a * b;}
    public float getmultiplicacion(float a, float b) { return a * b;}
    public double getmultiplicacion(double a, double b) { return a * b;}

    //Setters
    public void setmultiplicacion(int a, int b) {
         a * b;
    }
    public void setmultiplicacion(float a, float b) {
         a * b;
    }
    public void setmultiplicacion(double a, double b){
         a * b;
    }
}
